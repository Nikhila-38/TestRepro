        // we can execute the above or below values and can get the results by adding or removing the comments

        int a = 5;
        int b = 3;
        float c = 1.25f;

        System.out.println(a+b);
        System.out.println(a+c);
        System.out.println(a-b);
        System.out.println(a-c);
        System.out.println(a*b);
        System.out.println(a*c);
        System.out.println(a/b);
        System.out.println(a/c);
        System.out.println(a%b);
        System.out.println(a%c);


        System.out.println("a+b");// it will be displayed a+b because its in string format

