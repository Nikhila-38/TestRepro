package ex_004_Operators;

public class Lab032_Equal {
    public static void main(String[] args) {

        // == ---> compares both values

        System.out.println(20 == 20);
        System.out.println(20 < 20);
        System.out.println(10 == 10);
        System.out.println(20 >= 20);

    }
}
