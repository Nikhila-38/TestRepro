package ex_09_Switch_Case;

public class Lab087_Interview1 {
    public static void main(String[] args) {
        char code = 'C';

        switch (code){
            default :
            System.out.println("Hellooo");
            break;
            case 'A':
                System.out.println("52");
                break;
            case 'B':
                System.out.println("52");
                break;

        }
    }
}
